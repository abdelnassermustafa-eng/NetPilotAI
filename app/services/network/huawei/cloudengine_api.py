from typing import List

def generate_interface_config(interfaces: List[str]) -> str:
    """
    Generate basic interface configuration for Huawei CloudEngine switches.
    """

    lines = ["# Configuration generated by NetPilot AI (Huawei CE)"]

    for iface in interfaces:
        lines.append(f"interface {iface}")
        lines.append(" description Managed by NetPilot AI")
        lines.append(" undo shutdown")
        lines.append("#")
    
    return "\n".join(lines)
